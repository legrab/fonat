import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
const root = path.resolve(import.meta.dirname, '..');
const modulesDir = path.join(root, 'modules');
const imports = [];
const names = [];
for (const entry of (await readdir(modulesDir, { withFileTypes: true }))
  .filter((e) => e.isDirectory())
  .sort((a, b) => a.name.localeCompare(b.name))) {
  const pkg = JSON.parse(await readFile(path.join(modulesDir, entry.name, 'package.json'), 'utf8'));
  if (!pkg.fonatModule?.manifestExport) continue;
  const local = `manifest_${entry.name.replace(/[^a-zA-Z0-9]/g, '_')}`;
  imports.push(`import { ${pkg.fonatModule.manifestExport} as ${local} } from '${pkg.name}';`);
  names.push(local);
}
const source = `// Generated by scripts/generate-module-registry.mjs. Do not edit.\n${imports.join('\n')}\n\nexport const generatedInstalledModules = [${names.join(', ')}] as const;\n`;
await writeFile(path.join(root, 'packages/module-registry/src/generated.ts'), source);
console.log(`Generated registry for ${names.length} modules.`);
